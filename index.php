		<h1> 2. Bukit Campuhan </h1>
		<img src="media/BUkit-campuhan.jpg" class="img-rounded" alt="Cinque Terre" width="650" height="400"> 
        <p1>	Pemandangan Bukit Campuhan Ubud kini menjadi destinasi wisata yang populer di kalangan para wisatawan. Lokasinya yang tersembunyi, membuat tempat ini jadi lokasi favorit, terutama karena bisa sejenak merasakan kesunyian, jauh dari ingar bingar suasana perkotaan.

Tempat wisata Ubud satu ini tidak hanya menawarkan indahnya pemandangan alam. Refresing di bukit ini juga mampu mengusir segala kepenatan. Pemandangan asri di Bukit Campuhan Ubud semakin nampak asyik dengan jalur trekking yang tertata rapi sepanjang 2 kilometer.

Di kawasan Bukit Campuhan Ubud terdapat sebuah pura yang bernama Pura Gunung Lebah. Bukit tempat wisata Ubud ini diapit oleh dua anak sungai sehingga terbentuk lembah-lembah hijau yang rimbun oleh pepohonan tropis.

Rumput-rumput ilalang juga tumbuh dengan sangat baik sehingga sepanjang mata memandang hanya tampak hijaunya alam yang menyegarkan.Jika berencana datang ke Bukit Campuhan, baiknya datang pada pagi hari atau sore hari. Karena ketika pagi udara masih sangat segar dengan matahari yang hangat. Jangan datang ketika siang jika tidak ingin terpapar panasnya matahari karena tidak adanya tempat berteduh.

Alamat: Jalan Bangkiang Sidem, Kelusa, Payangan, Ubud, Kabupaten Gianyar
Tiket: Gratis
Jam Buka: 24 Jam </p1>
